const fs = require('fs')
const path = require('path')

const outDir = path.join(__dirname, '..', 'public', 'examples')

function tokens(input = 0, output = 0) {
  return { input, output, total: input + output }
}

function costUsd(input = 0, output = 0) {
  return {
    input: Number(((input * 2.5) / 1_000_000).toFixed(6)),
    output: Number(((output * 10) / 1_000_000).toFixed(6)),
    total: Number(((input * 2.5 + output * 10) / 1_000_000).toFixed(6)),
  }
}

function writeTrace(filename, trace) {
  fs.writeFileSync(path.join(outDir, filename), `${JSON.stringify(trace, null, 2)}\n`, 'utf8')
}

